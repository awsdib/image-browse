import {Post} from '../pages/gallery/gallery';
import * as URI from 'urijs';

export interface PostProvider {
  getPosts(offset?: number): Post[];
}

export class GelbooruService implements PostProvider {
  constructor(private siteAddress: string) {}

  getPosts(offset: number = 0): Post[] {
    let response = SAMPLE_DATA[this.siteAddress];

    if (!response) {
      // TODO: Provide an error message for unsupported site.
      return [];
    }

    let results = [];
    let counter = 0;
    for (let post of response) {
      let image = URI({
        protocol: 'http',
        hostname: this.siteAddress,
        path: `images/${post.directory}/${post.image}`,
      }).toString();

      let sample;
      if (post.sample) {
        sample = URI({
          protocol: 'http',
          hostname: this.siteAddress,
          path: `samples/${post.directory}/sample_${post.image}`,
        }).toString();
      } else {
        sample = image;
      }


      let thumbnail = URI({
        protocol: 'http',
        hostname: this.siteAddress,
        path: `thumbnails/${post.directory}/thumbnail_${post.image}`,
      }).toString();

      results.push({
        image: image,
        thumbnail: thumbnail,
        sample: sample,
        tags: post.tags.split(' '),
        index: counter,
      });

      counter += 1;
    }

    return results;
  }
}

